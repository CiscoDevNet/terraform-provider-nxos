func (r *AnalyticsResource) Read(ctx context.Context, req resource.ReadRequest, resp *resource.ReadResponse) {
	var state Analytics

	// Read state
	diags := req.State.Get(ctx, &state)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Read identity if available (requires Terraform >= 1.12.0)
	if req.Identity != nil && !req.Identity.Raw.IsNull() {
		var identity AnalyticsIdentity
		diags = req.Identity.Get(ctx, &identity)
		if resp.Diagnostics.Append(diags...); resp.Diagnostics.HasError() {
			return
		}
		state.fromIdentity(ctx, &identity)
	}

	tflog.Debug(ctx, fmt.Sprintf("%s: Beginning Read", state.Dn.ValueString()))

	device, ok := r.data.Devices[state.Device.ValueString()]
	if !ok {
		resp.Diagnostics.AddError("Client Error", fmt.Sprintf("Failed to find device '%s' in provider configuration", state.Device.ValueString()))
		return
	}

	if device.Managed {
		queries := []func(*nxos.Req){nxos.Query("rsp-subtree", "full"), nxos.Query("rsp-subtree-class", "analyticsInst,analyticsProfile,analyticsEvents,analyticsPolicy,analyticsMatchAcl,analyticsRecordP,analyticsCollector,analyticsMonitor,analyticsRsRecordPAtt,analyticsCollectorBucket,analyticsRsCollectorAtt,analyticsTrafficAnalytics,analyticsFwdInstTarget,analyticsRsMonitorAtt,analyticsRsProfAtt,analyticsRsEventsAtt,analyticsRsPolicyAtt")}
		res, err := device.Client.GetDn(state.Dn.ValueString(), queries...)
		if err != nil {
			resp.Diagnostics.AddError("Client Error", fmt.Sprintf("Failed to retrieve object, got error: %s", err))
			return
		}

		if !res.Exists() {
			resp.State.RemoveResource(ctx)
			return
		}

		imp, diags := helpers.IsFlagImporting(ctx, req.Private)
		if resp.Diagnostics.Append(diags...); resp.Diagnostics.HasError() {
			return
		}
		if imp {
			state.fromBody(res)
			// Warn here rather than in ModifyPlan: Terraform reuses this same refresh for both the
			// preview plan and the plan it recomputes right before applying, so Read only runs once
			// per operation - unlike ModifyPlan, which runs (and would warn) twice.
			resp.Diagnostics.AddWarning(
				"Import leniency in effect",
				"This resource was recently imported. If the plan shows entries or attributes disappearing, "+
					"that's expected: Terraform will stop tracking anything not declared in your configuration, "+
					"but nothing will change on the device. This notice won't appear again after your next apply.",
			)
		} else {
			state.updateFromBody(res)
		}
	}

	var identity AnalyticsIdentity
	identity.toIdentity(ctx, &state)

	tflog.Debug(ctx, fmt.Sprintf("%s: Read finished successfully", state.Dn.ValueString()))

	diags = resp.State.Set(ctx, &state)
	resp.Diagnostics.Append(diags...)
	diags = resp.Identity.Set(ctx, &identity)
	resp.Diagnostics.Append(diags...)
}

